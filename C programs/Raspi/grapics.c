int draw_mandelbrot_set(){

	int iteration;
	int max_iteration = 500;	
	
	printf("Computing...\n");
	gsl_matrix_ulong * m = gsl_matrix_ulong_alloc (1000, 1000);
	
	for (double i=0; i<m->size1; i++){
		for (double j=0; j<m->size2; j++){
			
			gsl_complex z = gsl_complex_rect(0, 0);
			gsl_complex c = gsl_complex_rect(((i / m->size1) * 2) - 1.5, ((j / m->size2) * 2) - 1);
			
			iteration = 0;
			while (gsl_complex_abs(z) < 4 && iteration++ < max_iteration){
				
				z = gsl_complex_add(c, gsl_complex_mul(z,z));
			}
			
//      printf("%f %f\n", x0, y0);
//			if (iteration >= 50)
//				gsl_matrix_ulong_set(m, i, j, palette_tab[iteration]);
			if (iteration < max_iteration)
				gsl_matrix_ulong_set(m, i, j, 0x000000);
			else
				gsl_matrix_ulong_set(m, i, j, 0xFFFFFF);
		}
	}

	print("img.png", m);
	
	return 0;
}

